let estacao = "verao";

switch (estacao) {
    case "verao":
        console.log("Muito calor");
        break;
    case "inverno":
        console.log("Muito frio");
        break;
    case "primavera":
        console.log("Flores");
        break;
    case "outono":
        console.log("Folhas caindo");
        break;
    default:
        console.log("Estação inválida");
}